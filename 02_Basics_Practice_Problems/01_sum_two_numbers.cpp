// Program to calculate the sum of two given numbers.
#include <iostream>
using namespace std;

int main()
{
    cout << "Welcome to Summing Two Number.\n";
    float a, b;

    cout << "Enter the First Number : ";
    cin >> a;

    cout << "Enter the Second Number : ";
    cin >> b;

    cout << "The Sum of Given Numbers is : " << a + b << endl;
    return 0;
}